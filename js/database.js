//get amount of items from obj
Object.size = function(obj) {
    var size = 0, key;
    for (key in obj) {
        if (obj.hasOwnProperty(key)) size++;
    }
    return size;
};

//size of obj
var size = Object.size(people);
// random number to add class active to
var randomStart = Math.floor(Math.random() * size);


function addListStyles( nextSlideIndex ) {

	$('.fp-slidesNav ul li a').each(function(){

		// clear ids
		$(this).attr('id', '');

		var thisIndex = Number($(this).attr('data-counter'));

		if( thisIndex === (nextSlideIndex - 2) ) {
				$(this).attr('id', 'left2');
				$(this).addClass('currentLinks').show();
		} else if ( thisIndex === (nextSlideIndex - 1) )  {
				$(this).attr('id', 'left1');
				$(this).addClass('currentLinks').show();
		} else if ( thisIndex === nextSlideIndex ) {
				// change title
				var thisTitle = people[thisIndex].titel;
				$('.top-nav a > p').text(thisTitle);

				// active
				$(this).attr('id', 'center');
				$(this).addClass('currentLinks').show();
		} else if ( thisIndex === (nextSlideIndex + 1) ) {
				$(this).attr('id', 'right1');
				$(this).addClass('currentLinks').show();
		} else if ( thisIndex === (nextSlideIndex + 2) ) {
				$(this).attr('id', 'right2');
				$(this).addClass('currentLinks').show();
		} else {
			$(this).removeClass().hide();
		}
	});
}

